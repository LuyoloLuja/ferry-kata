public class Car {
    private String color;
    private int passengerCount;

    public Car(String color, int passengerCount) {
        this.color = color;
        this.passengerCount = passengerCount;
    }

    public String getColor() {
        return color;
    }

    public int getPassengerCount() {
        return passengerCount;
    }
}
